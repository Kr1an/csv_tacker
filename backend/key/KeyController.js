var express = require('express');
var router = express.Router();
var bodyParser = require('body-parser');

router.use(bodyParser.urlencoded({ extended: true }));
router.use(bodyParser.json());
var Key = require('./Key');

// CREATES A NEW KEY
router.post('/', function (req, res) {
    const { value } = req.body;
    console.log(value);
    Key.create({ value }, 
        function (err, user) {
            if (err) return res.status(500).send("There was a problem adding the information to the database.");
            res.status(200).send(user);
        });
});

// RETURNS ALL THE KEYS IN THE DATABASE
router.get('/', function (req, res) {
    Key.find({}, function (err, users) {
        if (err) return res.status(500).send("There was a problem finding the users.");
        res.status(200).send(users);
    });
});

// GETS A SINGLE KEY FROM THE DATABASE
router.get('/:id', function (req, res) {
    Key.findById(req.params.id, function (err, user) {
        if (err) return res.status(500).send("There was a problem finding the user.");
        if (!user) return res.status(404).send("No user found.");
        res.status(200).send(user);
    });
});

// DELETES A KEY FROM THE DATABASE
router.delete('/:id', function (req, res) {
    Key.findByIdAndRemove(req.params.id, function (err, user) {
        if (err) return res.status(500).send("There was a problem deleting the user.");
        res.status(200).send("Key: "+ user.name +" was deleted.");
    });
});

// UPDATES A SINGLE KEY IN THE DATABASE
router.put('/:id', function (req, res) {
    Key.findByIdAndUpdate(req.params.id, req.body, {new: true}, function (err, user) {
        if (err) return res.status(500).send("There was a problem updating the user.");
        res.status(200).send(user);
    });
});


module.exports = router;